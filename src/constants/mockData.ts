function generateAgileRates(): AgileRate[] {
  const rates: AgileRate[] = [];
  const importCurve = [
    8.2, 7.5, 6.1, 5.3, 4.8, 5.5,
    7.8, 12.4, 18.6, 22.1, 24.3, 23.8,
    21.5, 19.2, 17.8, 16.5, 22.8, 28.4,
    35.2, 42.6, 48.1, 38.5, 29.3, 18.7,
    14.2, 11.8, 9.5, 8.8, 7.2, 6.5,
    5.9, 5.2, 6.8, 8.4, 10.2, 14.5,
    19.8, 25.6, 30.1, 34.8, 39.2, 36.4,
    31.2, 25.8, 20.5, 16.8, 12.4, 9.8,
  ];
  const exportCurve = [
    4.1, 3.8, 3.0, 2.7, 2.4, 2.8,
    3.9, 6.2, 9.3, 11.1, 12.2, 11.9,
    10.8, 9.6, 8.9, 8.3, 11.4, 14.2,
    17.6, 21.3, 24.1, 19.3, 14.7, 9.4,
    7.1, 5.9, 4.8, 4.4, 3.6, 3.3,
    3.0, 2.6, 3.4, 4.2, 5.1, 7.3,
    9.9, 12.8, 15.1, 17.4, 19.6, 18.2,
    15.6, 12.9, 10.3, 8.4, 6.2, 4.9,
  ];
  for (let i = 0; i < 48; i++) {
    const hour = Math.floor(i / 2);
    const minute = (i % 2) * 30;
    const time = `${String(hour).padStart(2, '0')}:${String(minute).padStart(2, '0')}`;
    rates.push({ time, hour, minute, rate: importCurve[i], isExport: false });
  }
  for (let i = 0; i < 48; i++) {
    const hour = Math.floor(i / 2);
    const minute = (i % 2) * 30;
    const time = `${String(hour).padStart(2, '0')}:${String(minute).padStart(2, '0')}`;
    rates.push({ time, hour, minute, rate: exportCurve[i], isExport: true });
  }
  return rates;
}

